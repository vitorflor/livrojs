const prompt = require("prompt-sync")()
const pessoas = Number(prompt("NÃºmero de pessoas:"))
const peixes = Number(prompt("Numero de peixes:"))
let pagar
if(peixe <= pessoas) {
pagar = pessoas * 20
} else {
const contadepessoas = pessoas * 20
const contadopeixe = (pessoas - peixes) *12 
pagar = contadepessoas + contadopeixe
}
console.log(`Pagar: ${apagar.toFixed(2)}`)